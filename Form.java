    private void cmbProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProdutosActionPerformed
        // TODO add your handling code here:
        String valor = "";
        String produto = cmbProdutos.getSelectedItem().toString();
        
        switch (produto){
            case "Salgado Frito":
                valor = "5.0";
                break;
            case "Salgado Assado":
                valor = "6.0";
                break;
            case "Refri Lata":
                valor = "8.0";
                break;
            case "Suco Natural":
                valor = "10.0";
                break;
        }
        
        txtValor.setText(valor);
    }//GEN-LAST:event_cmbProdutosActionPerformed

    private void BtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAdicionarActionPerformed
        // TODO add your handling code here:
        String produto = cmbProdutos.getSelectedItem().toString();
        Double valor = Double.parseDouble(txtValor.getText());
        int quantidade = Integer.parseInt(jsQuantidade.getValue().toString());
        
        DefaultTableModel tabela = (DefaultTableModel) tblProdutos.getModel();
        Object[] novaLinha = new Object[] {
            produto,
            valor,
            quantidade,
            valor * quantidade 
        };
        tabela.addRow(novaLinha);
        calcularTotal();
        
        cmbProdutos.setSelectedIndex(0);
        txtValor.setText("5.0");
        jsQuantidade.setValue(0);
    }//GEN-LAST:event_BtnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblProdutos.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela");
        }
        else{
            int op = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este produto?");
            if(op == 0) {
               DefaultTableModel tabela = (DefaultTableModel) tblProdutos.getModel();
               tabela.removeRow(linhaSelecionada);
               calcularTotal();
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

