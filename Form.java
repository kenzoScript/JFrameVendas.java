    private void calcularTotal(){
        
        double total = 0;
        for(int i = 0; i < tblProdutos.getRowCount(); i++){
            String sValor = tblProdutos.getValueAt(i, 3).toString();
            double valor = Double.parseDouble(sValor);
            total += valor;
        }
        
        txtTotal.setText(String.valueOf(total));
    }

